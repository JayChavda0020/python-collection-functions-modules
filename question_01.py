# What is List? How will you reverse a list ?

''' ans - Lists are used to store multiple items in a single variable. items in the list are can be of any data type'''

l = ["python", "java", "html", "css", "javascript", True, 21, False]

print("Original list is : ", l)

rev_l = l[::-1]

print("*"*50)

print("Reversed list is : ", rev_l)